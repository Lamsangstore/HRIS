// ชั่วโมงลา + การแสดงผลวัน/ชั่วโมง
// บั๊กที่เคยเกิด: ลาเช้า 08:00–12:00 ได้ 3.56 ชม. เพราะหักพักเที่ยงแบบเฉลี่ยตามสัดส่วน
// ทั้งที่ช่วงเช้าไม่ได้คาบเกี่ยวเวลาพักเลย
import { loadFns, makeChecker } from './extract.mjs';

const { calcLeaveHours, getDayWorkHours, hoursToDisplay, balanceToDisplay } = loadFns(
    ['getDayWorkHours', 'hhmmToMins', 'getBreakWindow', 'breakOverlapMins',
     'calcLeaveHours', 'splitDaysHours', 'hoursToDisplay', 'balanceToDisplay'],
    { isPublicHoliday: () => false },
);

const check = makeChecker();
const S = { workDays: [1,2,3,4,5], workStart: '08:00', workEnd: '17:00', breakStart: '12:00', breakMinutes: 60 };

// ── ชั่วโมงลา ────────────────────────────────────────────────────────────────
const hrs = (sd, st, ed, et, sc = S) => calcLeaveHours(sd, st, ed, et, sc);

check('ลาเช้า 08:00–12:00 ไม่คาบพักเที่ยง → 4 ชม. เต็ม', hrs('2026-07-17','08:00','2026-07-17','12:00'), 4);
check('ลาบ่าย 13:00–17:00 → 4 ชม.',                      hrs('2026-07-17','13:00','2026-07-17','17:00'), 4);
check('ลาเต็มวัน → 8 ชม. (หักพัก 1 ชม.)',                 hrs('2026-07-17','08:00','2026-07-17','17:00'), 8);
check('ลาเต็มวันต้องเท่ากับ ชม./วัน ที่ตั้งไว้',            hrs('2026-07-17','08:00','2026-07-17','17:00'), getDayWorkHours(S));
check('ลาคร่อมพักเที่ยง 11:00–14:00 → หักพักออก 1 ชม.',   hrs('2026-07-17','11:00','2026-07-17','14:00'), 2);
check('ลาอยู่ในเวลาพักล้วน 12:00–12:30 → 0',              hrs('2026-07-17','12:00','2026-07-17','12:30'), 0);
check('ลา 1 ชม. 09:00–10:00',                            hrs('2026-07-17','09:00','2026-07-17','10:00'), 1);
check('ลา 2 วันเต็ม จ.–อ. → 16 ชม.',                      hrs('2026-07-20','08:00','2026-07-21','17:00'), 16);
check('ลา ศ.–จ. ต้องข้ามเสาร์อาทิตย์ → 16 ชม.',            hrs('2026-07-17','08:00','2026-07-20','17:00'), 16);
check('ไม่มีเวลาพัก (breakMinutes=0)',                    hrs('2026-07-17','08:00','2026-07-17','12:00', {...S, breakMinutes: 0}), 4);
check('กะ 08:30–17:30 พักเที่ยง → ลา 08:30–12:00 = 3.5',   hrs('2026-07-17','08:30','2026-07-17','12:00', {...S, workStart:'08:30', workEnd:'17:30'}), 3.5);
check('schedule เก่าที่ยังไม่มี breakStart → ใช้ 12:00 เป็นค่าตั้งต้น',
      hrs('2026-07-17','08:00','2026-07-17','12:00', {workDays:[1,2,3,4,5], workStart:'08:00', workEnd:'17:00', breakMinutes:60}), 4);

// ── แสดงโควต้า/คงเหลือ: บอกครบทั้งสองหน่วยเสมอ ────────────────────────────────
check('โควต้าลาพักร้อน 16 วัน → "16 วัน 0 ชม."',  balanceToDisplay(128, S), '16 วัน 0 ชม.');
check('คงเหลือ 13.5 วัน → "13 วัน 4 ชม."',        balanceToDisplay(108, S), '13 วัน 4 ชม.');
check('ใช้ไป 2.5 วัน → "2 วัน 4 ชม."',            balanceToDisplay(20,  S), '2 วัน 4 ชม.');
check('ยังไม่ใช้เลย → "0 วัน 0 ชม."',             balanceToDisplay(0,   S), '0 วัน 0 ชม.');
check('ปัดเศษแล้วครบวันพอดี ต้องไม่เป็น "0 วัน 8 ชม."', balanceToDisplay(7.99, S), '1 วัน 0 ชม.');
check('โควต้าติดลบ ต้องไม่ขึ้น "-0 วัน 4 ชม."',   balanceToDisplay(-4, S), 'ติดลบ 0 วัน 4 ชม.');
check('ไม่มีค่า → "-"',                           balanceToDisplay(null, S), '-');

// ── แสดงระยะเวลาใบลาแต่ละใบ: ตัดหน่วยที่เป็น 0 ออก ───────────────────────────
check('ใบลาครึ่งวัน → "4 ชม." ไม่ใช่ "0 วัน 4 ชม."', hoursToDisplay(4,  S), '4 ชม.');
check('ใบลาเต็มวัน → "1 วัน" ไม่ใช่ "1 วัน 0 ชม."',   hoursToDisplay(8,  S), '1 วัน');
check('ใบลา 1 วันครึ่ง → "1 วัน 4 ชม."',             hoursToDisplay(12, S), '1 วัน 4 ชม.');

check.done('ชั่วโมงลา');
